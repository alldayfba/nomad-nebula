# ── Writer ────────────────────────────────────────────────────────────────────

def write_file(path: Path, content: str) -> None:
    path.write_text(content, encoding="utf-8")
    path.chmod(0o664)
    print(f"  ✓ {path.name} ({len(content):,} chars, {len(content.splitlines())} lines)")


# ── Helpers ───────────────────────────────────────────────────────────────────

def _frontmatter(text: str, key: str) -> str:
    m = re.search(rf"^{key}:\s*(.+)$", text, re.MULTILINE)
    return m.group(1).strip().strip('"') if m else ""


def _first_paragraph(text: str) -> str:
    for line in text.splitlines():
        line = line.strip()
        if line and not line.startswith("#") and not line.startswith(">") and not line.startswith("---"):
            return line[:100]
    return ""


def _extract_docstring_purpose(text: str) -> str:
    """Get the Purpose line from a script's module docstring only.

    MED-5: Previous version searched the full file, causing regex source code
    inside the file body to leak into the generated TOOLS.md descriptions.
    Now restricted to the first 1500 chars and searches within the extracted
    docstring content only — never the surrounding source code.
    """
    header = text[:1500]
    # Extract module-level docstring content
    m = re.search(r'"""(.*?)"""', header, re.DOTALL)
    if not m:
        m = re.search(r"'''(.*?)'''", header, re.DOTALL)
    if not m:
        return ""
    docstring = m.group(1)
    # Look for Purpose: label within docstring text
    pm = re.search(r'Purpose:\s*(.+)', docstring)
    if pm:
        result = pm.group(1).strip()
        if result:
            return result
    # Fall back to first non-empty line of docstring
    for line in docstring.splitlines():
        line = line.strip()
        if line:
            return line[:100]
    return ""


# ── Main ──────────────────────────────────────────────────────────────────────

def main() -> None:
    args      = sys.argv[1:]
    do_tools  = "--tools" in args or not args
    do_agents = "--agents" in args or not args

    print(f"Regenerating OpenClaw workspace files...")

    skills  = parse_skills()
    bots    = parse_bots()
    scripts = parse_scripts() if do_tools else []

    print(f"  Found: {len(skills)} skills, {len(bots)} bots, {len(scripts)} scripts")

    if do_tools:
        content = generate_tools_md(skills, scripts)
        write_file(OC_WORKSPACE / "TOOLS.md", content)

    if do_agents:
        content = generate_agents_md(skills, bots)
        write_file(OC_WORKSPACE / "AGENTS.md", content)

    print("Done.")


if __name__ == "__main__":
    main()
