# ── Parsers ───────────────────────────────────────────────────────────────────

def parse_skills() -> list[dict]:
    """Extract name, description, trigger from every skill .md file."""
    skills = []
    for f in sorted(SKILLS_DIR.glob("*.md")):
        if f.name.startswith("_"):
            continue
        text = f.read_text(encoding="utf-8")
        name  = _frontmatter(text, "name") or f.stem
        desc  = _frontmatter(text, "description") or ""
        trigger = _frontmatter(text, "trigger") or ""
        skills.append({"name": name, "description": desc, "trigger": trigger})
    return skills


def parse_bots() -> list[dict]:
    """Extract bot name and purpose from identity.md in each bot dir."""
    bots = []
    for bot_dir in sorted(BOTS_DIR.iterdir()):
        if not bot_dir.is_dir() or bot_dir.name.startswith("_"):
            continue
        identity_file = bot_dir / "identity.md"
        if not identity_file.exists():
            continue
        text    = identity_file.read_text(encoding="utf-8")
        # First non-empty, non-frontmatter line as description
        purpose = _first_paragraph(text)
        bots.append({"name": bot_dir.name, "purpose": purpose})
    return bots


def parse_scripts() -> list[dict]:
    """Extract script name and one-line purpose from docstring."""
    scripts = []
    for f in sorted(EXECUTION_DIR.glob("*.py")):
        if f.name.startswith("_"):
            continue
        text   = f.read_text(encoding="utf-8", errors="ignore")
        purpose = _extract_docstring_purpose(text)
        scripts.append({"name": f.name, "purpose": purpose})
    return scripts


